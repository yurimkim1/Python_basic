# 사칙연산
a = 3
b = 4
print(a+b)

print(a*b)

print(a/b)

# **연산자
# 이 연산자는 x ** y 처럼 사용 했을 때
# x의 y제곱(x의y승)값을 돌려준다
a = 3
b = 4
print(a**b)

# %연산자
# 나눗셈의 나머지 값을 돌려주는 연산자
# 7을 3으로 나누면 나머지는 1이 되고 3을 7로 나누면 나머지는 3
print(7%3)

print(3%7)

# // 연산자
# 나눗셈 후 몫을 반환
print(7/4)

# 1.75에서 몫에 해당하는 정수값 1만 돌려준다
print(7//4)